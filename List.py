# Запрашиваем имена пользователя
имена = []
for i in range(5):
    имя = input("Дайте имя!: ")
    имена.append(имя)
имена.sort()                           # Сортируем имена в алфавитном порядке,далее выводим имена на экран
print("Имена по алфавиту: ")
for имя in имена:
    print(имя)
# Выводим последнее добавленное имя
print("Последнее имя: ", имена[-1])


