#создаем список с именами студентов,в котором есть повторяющиеся имена
студенты = ['Juhan','Kati','Mario','Mario','Mati','Mati']
уникальные_имена = []
for студент in студенты:
    if студент not in уникальные_имена:
        уникальные_имена.append(студент)
